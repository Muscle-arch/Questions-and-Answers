/**
 * 根据关键词搜索知识库
 * @param {string} query - 用户查询
 * @returns {Array} - 匹配的问答对
 */
export function searchKnowledgeBase(query) {
  if (!query) return [];
  
  const lowerQuery = query.toLowerCase();
  
  return KNOWLEDGE_BASE.filter((item) => {
    // 匹配问题
    if (item.question.toLowerCase().includes(lowerQuery)) return true;
    // 匹配关键词
    if (item.keywords.some((kw) => lowerQuery.includes(kw.toLowerCase()))) return true;
    // 匹配分类
    if (item.category.includes(lowerQuery)) return true;
    return false;
  });
}

/**
 * 根据ID获取知识库条目
 * @param {number} id - 知识库条目ID
 * @returns {Object|null}
 */
export function getKnowledgeById(id) {
  return KNOWLEDGE_BASE.find((item) => item.id === id) || null;
}

/**
 * 获取所有分类
 * @returns {Array}
 */
export function getCategories() {
  return [...new Set(KNOWLEDGE_BASE.map((item) => item.category))];
}

/**
 * 根据分类获取问答对
 * @param {string} category - 分类名称
 * @returns {Array}
 */
export function getKnowledgeByCategory(category) {
  return KNOWLEDGE_BASE.filter((item) => item.category === category);
}

